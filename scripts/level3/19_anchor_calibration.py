#!/usr/bin/env python3
"""
Level 3 — Step 19: Process the synthetic anchor JDs through the Stage-1
labelling + embedding pipeline and compute their Method B fit_score
against every CV. The resulting `anchors_summary.csv` provides the
per-construct corpus anchors used by the threshold logic in
`20_nominal_scoring.py`.

Two anchor types (generated by script 18, stored as raw .txt under
`data/synthetic_anchors/`):

  max_anchor[cv]  =  Method B fit_score of `cv` against the CV-specific
                     synthetic JD generated by script 18
                     (data/synthetic_anchors/max_{cv_id}.txt).
                     Empirical CEILING — what the candidate would score
                     against a JD that asks for exactly the work they
                     already do.

  min_anchor      =  Method B fit_score of every CV against the
                     universal out-of-domain JD
                     (min_space_nurse.txt).
                     Empirical FLOOR — what each CV gets against a JD
                     with no meaningful overlap.

What this script does end-to-end:
  1. Labels each synthetic JD (gpt-4o-mini, reusing the prompt and
     few-shot examples from `03_llm_label_json.py`).
  2. Saves canonical labelled JSON in
     `results/level3/labelled/anchors/{jd_name}.json` (plus a joined
     CSV alongside for inspection).
  3. Embeds the labelled segments with `all-MiniLM-L6-v2`.
  4. Computes Method B fit_score for every (cv, anchor_jd) pair, using
     the SAME aggregation rules as `14_maxsim_scoring.py`
     (mixed weight 0.5; education fallback = mean(skills, experience)
     when JD has no edu requirement; final formula 0.6/0.3/0.1).
  5. Writes `results/level3/anchors_summary.csv` (12 anchor pairs with
     per-block similarities + final fit_score).

This script does NOT compute calibrated 0–100 scores. Score
transformation (cosine → 0–100) is handled by
`20_nominal_scoring.py`, which uses the per-construct anchor ranges
written here to seed the education threshold while skills + experience
thresholds come from L2 κ-calibration.

Run:
    cd ~/Documents/cool-cohen/llm_evaluation
    python3 scripts/level3/19_anchor_calibration.py
"""

import csv
import importlib.util
import json
from pathlib import Path

import numpy as np
import pandas as pd
from openai import OpenAI
from sentence_transformers import SentenceTransformer


ROOT       = Path(__file__).parent.parent.parent
CV_DIR     = ROOT / "results" / "level3" / "labelled" / "cv"
ANCHOR_DIR = ROOT / "data" / "synthetic_anchors"
OUT_LBL    = ROOT / "results" / "level3" / "labelled" / "anchors"

ANCHORS_CSV = ROOT / "results" / "level3" / "anchors_summary.csv"

MODEL_EMBED = "sentence-transformers/all-MiniLM-L6-v2"
ROUND_TO    = 5

CV_IDS = ["cv_primary", "cv_hr", "cv_engineer"]
MAX_ANCHOR_FOR = {
    "cv_primary":  "max_cv_primary",
    "cv_hr":       "max_cv_hr",
    "cv_engineer": "max_cv_engineer",
}
MIN_ANCHOR_NAME  = "min_space_nurse"
ALL_ANCHOR_NAMES = list(MAX_ANCHOR_FOR.values()) + [MIN_ANCHOR_NAME]

# Method B aggregation constants — must match script 14.
MIXED_WEIGHT = 0.5
PURE_WEIGHT  = 1.0
W_SKILLS = 0.6
W_EXP    = 0.3
W_EDU    = 0.1


# ── Reuse Stage-1 labelling pipeline (script 03) ────────────────────────────
_SPEC = importlib.util.spec_from_file_location(
    "label_json", Path(__file__).parent / "03_llm_label_json.py"
)
_M = importlib.util.module_from_spec(_SPEC)
_SPEC.loader.exec_module(_M)

SYSTEM_PROMPT          = _M.SYSTEM_PROMPT
build_user_prompt      = _M.build_user_prompt
strip_blanks_to_json   = _M.strip_blanks_to_json
load_openai_key        = _M.load_openai_key
load_examples          = _M.load_examples
call_openai_with_retry = _M.call_openai_with_retry
autofill_missing       = _M.autofill_missing
validate               = _M.validate


# ── Method B aggregation (same logic as script 14) ──────────────────────────
def construct_block(sim: np.ndarray,
                    cv_tags: np.ndarray,
                    jd_tags: np.ndarray,
                    construct: str) -> float:
    allowed_cv = np.isin(cv_tags, [construct, "mixed"])
    if not allowed_cv.any():
        return float("nan")
    sub_sim = sim[allowed_cv, :]
    pure  = jd_tags == construct
    mixed = jd_tags == "mixed"
    if not (pure.any() or mixed.any()):
        return float("nan")
    ws, total = 0.0, 0.0
    if pure.any():
        ws    += float(sub_sim[:, pure].max(axis=0).sum()) * PURE_WEIGHT
        total += float(pure.sum()) * PURE_WEIGHT
    if mixed.any():
        ws    += float(sub_sim[:, mixed].max(axis=0).sum()) * MIXED_WEIGHT
        total += float(mixed.sum()) * MIXED_WEIGHT
    return ws / total if total else float("nan")


def education_block(sim: np.ndarray, cv_tags: np.ndarray,
                    jd_tags: np.ndarray) -> float:
    jd_edu = jd_tags == "education"
    cv_edu = cv_tags == "education"
    if not jd_edu.any() or not cv_edu.any():
        return float("nan")
    block = sim[np.ix_(cv_edu, jd_edu)]
    return float(block.max(axis=0).mean()) if block.size else float("nan")


def method_b_fit_score(cv_emb: np.ndarray, cv_tags: np.ndarray,
                       jd_emb: np.ndarray, jd_tags: np.ndarray) -> dict:
    sim = (cv_emb @ jd_emb.T).astype(np.float64)
    sim = np.clip(sim, -1.0, 1.0)

    sk = construct_block(sim, cv_tags, jd_tags, "skills")
    ex = construct_block(sim, cv_tags, jd_tags, "experience")

    jd_edu_mask = jd_tags == "education"
    cv_edu_mask = cv_tags == "education"
    if not jd_edu_mask.any():
        valid = [v for v in (sk, ex) if not np.isnan(v)]
        edu = float(np.mean(valid)) if valid else float("nan")
    elif cv_edu_mask.any():
        edu = education_block(sim, cv_tags, jd_tags)
    else:
        edu = float("nan")

    fit = (W_SKILLS * sk + W_EXP * ex + W_EDU * edu
           if not any(np.isnan([sk, ex, edu])) else float("nan"))

    return {
        "skills_block":     None if np.isnan(sk)  else round(sk,  4),
        "experience_block": None if np.isnan(ex)  else round(ex,  4),
        "education_block":  None if np.isnan(edu) else round(edu, 4),
        "fit_score":        None if np.isnan(fit) else round(fit, 4),
    }


# ── Label + embed one synthetic anchor JD ───────────────────────────────────
def label_and_embed(jd_name: str, examples,
                    client: OpenAI, model_embed: SentenceTransformer
                    ) -> tuple[np.ndarray, np.ndarray, list[dict]]:
    src = ANCHOR_DIR / f"{jd_name}.txt"
    raw = src.read_text(encoding="utf-8")
    target_input = strip_blanks_to_json(raw)
    prompt = build_user_prompt(examples, target_input)

    data, _ = call_openai_with_retry(client, SYSTEM_PROMPT, prompt)
    autofill_missing(target_input, data)
    v = validate(target_input, data)
    if not v["all_ok"]:
        print(f"  ! {jd_name} validation: {v}")

    tag_by_id = {row["line_id"]: row.get("tag", "other")
                 for row in data["labels"]}

    texts = [r["text"] for r in target_input]
    embs  = model_embed.encode(texts, normalize_embeddings=True,
                               show_progress_bar=False, batch_size=64)
    embs  = embs.astype(np.float32)

    segments = []
    for r in target_input:
        segments.append({
            "line_id":  r["line_id"],
            "text":     r["text"],
            "tag":      tag_by_id.get(r["line_id"], "other"),
            "embedding": [round(float(x), ROUND_TO) for x in embs[r["line_id"]]],
        })

    OUT_LBL.mkdir(parents=True, exist_ok=True)
    rec = {
        "id":         jd_name,
        "type":       "jd_anchor",
        "pool":       "synthetic_anchor",
        "n_lines":    len(segments),
        "tag_counts": {t: sum(1 for s in segments if s["tag"] == t)
                       for t in ["skills", "experience", "education", "mixed", "other"]},
        "embedding_meta": {"model": MODEL_EMBED, "dim": 384,
                           "normalised": True, "round_to": ROUND_TO},
        "segments":   segments,
    }
    (OUT_LBL / f"{jd_name}.json").write_text(
        json.dumps(rec, ensure_ascii=False, indent=2), encoding="utf-8")

    csv_path = OUT_LBL / f"{jd_name}.csv"
    with csv_path.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["line_id", "tag", "text"])
        for s in segments:
            w.writerow([s["line_id"], s["tag"], s["text"]])

    keep = [s for s in segments if s["tag"] != "other"]
    if not keep:
        return np.zeros((0, 384), dtype=np.float32), np.array([]), segments
    emb_keep = np.array([s["embedding"] for s in keep], dtype=np.float32)
    tag_keep = np.array([s["tag"]       for s in keep])
    return emb_keep, tag_keep, segments


def load_cv_non_other(cv_id: str) -> tuple[np.ndarray, np.ndarray]:
    rec = json.loads((CV_DIR / f"{cv_id}.json").read_text(encoding="utf-8"))
    keep = [s for s in rec["segments"] if s["tag"] != "other"]
    emb = np.array([s["embedding"] for s in keep], dtype=np.float32)
    tag = np.array([s["tag"]       for s in keep])
    return emb, tag


# ── Main ────────────────────────────────────────────────────────────────────
def main() -> None:
    print("Loading models / prompts ...")
    client      = OpenAI(api_key=load_openai_key())
    examples    = load_examples()
    embed_model = SentenceTransformer(MODEL_EMBED)

    # 1. Label + embed each of the 4 synthetic anchor JDs
    print("\nLabelling + embedding synthetic anchor JDs ...")
    anchor_data: dict[str, tuple[np.ndarray, np.ndarray]] = {}
    for jd_name in ALL_ANCHOR_NAMES:
        emb, tag, segs = label_and_embed(jd_name, examples, client, embed_model)
        non_other = sum(1 for s in segs if s["tag"] != "other")
        print(f"  ✓ {jd_name:<24}  {len(segs)} lines  (non-other: {non_other})")
        anchor_data[jd_name] = (emb, tag)

    # 2. Compute Method B fit_score for every (cv × anchor) pair
    print("\nComputing Method B fit_score for anchor pairs ...")
    rows = []
    for cv_id in CV_IDS:
        cv_emb, cv_tag = load_cv_non_other(cv_id)
        for jd_name in ALL_ANCHOR_NAMES:
            jd_emb, jd_tag = anchor_data[jd_name]
            res = method_b_fit_score(cv_emb, cv_tag, jd_emb, jd_tag)
            res.update({
                "cv_id":       cv_id,
                "anchor_jd":   jd_name,
                "anchor_role": "max" if jd_name == MAX_ANCHOR_FOR.get(cv_id)
                                else ("min" if jd_name == MIN_ANCHOR_NAME
                                      else "off_domain_max"),
            })
            rows.append(res)
            print(f"  {cv_id:<13} × {jd_name:<24}  "
                  f"fit_score = {res['fit_score']}  "
                  f"(skills={res['skills_block']}, "
                  f"exp={res['experience_block']}, "
                  f"edu={res['education_block']})")

    anc_df = pd.DataFrame(rows)[
        ["cv_id", "anchor_jd", "anchor_role",
         "skills_block", "experience_block", "education_block",
         "fit_score"]
    ]
    anc_df.to_csv(ANCHORS_CSV, index=False)
    print(f"\n✓ Anchor summary → {ANCHORS_CSV.relative_to(ROOT)}")

    # 3. Diagnostic — per-CV anchor ranges and corpus means per construct
    def lookup(cv: str, jd_name: str, col: str) -> float:
        sel = anc_df[(anc_df["cv_id"] == cv) & (anc_df["anchor_jd"] == jd_name)]
        return float(sel[col].iloc[0])

    print("\nPer-CV diagnostics (raw fit_score):")
    print(f"  {'cv_id':<14}  {'min':>8}  {'max':>8}  {'range':>8}")
    for cv_id in CV_IDS:
        mx = lookup(cv_id, MAX_ANCHOR_FOR[cv_id], "fit_score")
        mn = lookup(cv_id, MIN_ANCHOR_NAME,       "fit_score")
        print(f"  {cv_id:<14}  {mn:>8.4f}  {mx:>8.4f}  {mx - mn:>8.4f}")

    print("\nPer-construct corpus anchors (mean across 3 CVs) — used by script 20:")
    print(f"  {'block':<18}  {'min':>8}  {'max':>8}  {'range':>8}")
    for b in ("skills_block", "experience_block", "education_block"):
        mn = float(np.mean([lookup(cv, MIN_ANCHOR_NAME,       b) for cv in CV_IDS]))
        mx = float(np.mean([lookup(cv, MAX_ANCHOR_FOR[cv],    b) for cv in CV_IDS]))
        print(f"  {b:<18}  {mn:>8.4f}  {mx:>8.4f}  {mx - mn:>8.4f}")

    print("\nNext step: run `python3 scripts/level3/20_nominal_scoring.py`")


if __name__ == "__main__":
    main()
